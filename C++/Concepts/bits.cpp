#include<bits/stdc++.h>
using namespace std;
int main(){

    unsigned int x = 1;
    cout<<"Bitwise Not of unsigned int "<<x<<" = "<<(~x)<<endl;
    x = 5;
    cout<<"Bitwise Not of unsigned int "<<x<<" = "<<(~x)<<endl;

    int y=1;
    cout<<"Bitwise Not of signed int "<<y<<" = "<<(~y)<<endl;
    y = 5;
    cout<<"Bitwise Not of signed int "<<y<<" = "<<(~y)<<endl;

    return 0;
}